// Assignment 4: Products Array
// Declare an array of products
let products = ['Apple', 'Orange', 'Banana', 'Lemon', 'Jack Fruit'];

// Display the products using a for loop
for (let i = 0; i < products.length; i++) {
    console.log(products[i]);
}

console.log('-------------------');

// Display the products using a while loop
let j = 0;
while (j < products.length) {
    console.log(products[j]);
    j++;
}